export function getSleepSchedule(ageMonths: number): SleepSchedule | null {
  return (
    SLEEP_SCHEDULES.find(
      (s) => ageMonths >= s.minMonths && ageMonths < s.maxMonths
    ) ?? SLEEP_SCHEDULES[SLEEP_SCHEDULES.length - 1]
  );
}
