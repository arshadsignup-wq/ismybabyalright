export function getSuggestionsForAge(ageMonths: number): MealSuggestion[] {
  return mealSuggestions.filter(
    (s) => ageMonths >= s.minMonths && ageMonths <= s.maxMonths
  );
}

export function getSuggestionsByMealType(
  mealType: MealSuggestion["mealType"],
  ageMonths: number
): MealSuggestion[] {
  return mealSuggestions.filter(
    (s) =>
      s.mealType === mealType &&
      ageMonths >= s.minMonths &&
      ageMonths <= s.maxMonths
  );
}

export function getSuggestionById(id: string): MealSuggestion | undefined {
  return mealSuggestions.find((s) => s.id === id);
}

